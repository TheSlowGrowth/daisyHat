
def printBigHeadline(text):
    print("")
    print("#######################################################################")
    print(text)
    print("#######################################################################")
    print("")


def printSmallHeadline(text):
    print("")
    print("-----------------------------------------------------------------------")
    print(text)
    print("-----------------------------------------------------------------------")
    print("")

def printWarning(text):
    print("WARNING: " + str(text))

def printInfo(text):
    print("INFO: " + str(text))